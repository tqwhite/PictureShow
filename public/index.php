<?php

define('DOCROOT_DIRECTORY_PATH', realpath(dirname(__FILE__)).'/'); //still unused I think, could be wrong, too
define('SLIDESHOW_DIRECTORY_PATH', DOCROOT_DIRECTORY_PATH."media/slideshows/");
define('SLIDESHOW_RELURL_PREFIX', "/media/slideshows/");

// Define path to application directory
defined('APPLICATION_PATH')
    || define('APPLICATION_PATH', realpath(dirname(__FILE__) . '/../application'));

// Define application environment
defined('APPLICATION_ENV')
    || define('APPLICATION_ENV', (getenv('APPLICATION_ENV') ? getenv('APPLICATION_ENV') : 'production'));

// Ensure library/ is on include_path
set_include_path(implode(PATH_SEPARATOR, array(
    realpath(APPLICATION_PATH . '/../library'),
    get_include_path(),
)));

/** Zend_Application */
require_once 'Zend/Application.php';

// Create application, bootstrap, and run
$application = new Zend_Application(
    APPLICATION_ENV,
    APPLICATION_PATH . '/configs/application.ini'
);
$application->bootstrap()
            ->run();